function holdAnim()
	local ped = PlayerPedId()
	local dict = "anim@heists@box_carry@"
	local anim = "idle"
	loadAnimDict(dict)
	TaskPlayAnim(ped, dict, anim, 3.0, -1, -1, 50, 0, false, false, false)
	Citizen.CreateThread(function()
		while holdingPackage do
			if not IsEntityPlayingAnim(ped, dict, anim, 3) then
				TaskPlayAnim(ped, dict, anim, 4.0, 1.0, -1,49,0, 0, 0, 0)
				DisableControlAction(1, 140, true) --Disables Melee Actions
				DisableControlAction(1, 141, true) --Disables Melee Actions
				DisableControlAction(1, 142, true) --Disables Melee Actions	
			end
			Citizen.Wait(100)
		end
	end)
end

function removeAttachedProp()
	local playerPed = PlayerPedId()
	local coords    = GetEntityCoords(playerPed)
	local closestDistance = -1
	closestEntity   = 0
	for k , user in pairs(attachPropList) do
		local object = GetClosestObjectOfType(coords, 1.5, GetHashKey(attachPropList[k].model), false, false, false)
		if DoesEntityExist(object) then
			local objCoords = GetEntityCoords(object)
			local distance  = GetDistanceBetweenCoords(coords, objCoords, true)
			if closestDistance == -1 or closestDistance > distance then
				closestDistance = distance
				closestEntity   = object
				DeleteEntity(closestEntity)
				ClearPedTasks(PlayerPedId())
				ClearPedSecondaryTask(PlayerPedId())
			end
		end
	end
end

function tablelength(T)
	local count = 0
	for _ in pairs(T) do count = count + 1 end
	return count
  end

