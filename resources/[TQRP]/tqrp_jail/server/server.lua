RegisterServerEvent("tqrp_jail:search")
AddEventHandler("tqrp_jail:search", function(i, searched)
	local src = source
	local xPlayer = ESX.GetPlayerFromId(src)
	if not searched then
		Config.Search[i].isSearched = true
		local chance = math.random(1, 1000)
		if chance == 1 then
			local item = UltraRareItems[math.random(1, #UltraRareItems)]
			xPlayer.addInventoryItem(item.id, item.quantity)
		elseif chance < 250 then
			local item = RareItems[math.random(1, #RareItems)]
			xPlayer.addInventoryItem(item.id, item.quantity)
		else
			local item = Items[math.random(1, #Items)]
			xPlayer.addInventoryItem(item.id, item.quantity)
		end
	end
end)